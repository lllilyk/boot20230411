-- DEFAULT : 기본값 정할 수 있음
CREATE TABLE MyTable14 (
	Col1 INT,
    Col2 INT DEFAULT 99
);

INSERT INTO MyTable14 (Col1, Col2)
VALUES (100, 200);

-- Col2에는 기본값 99가 입력됨
INSERT INTO MyTable14 (Col1)
VALUES (100);

-- Col1에는 기본값이 없으므로 null
INSERT INTO MyTable14 (Col2)
VALUES (200);

SELECT * FROM MyTable14;

